package com.tf.tickfinder.model;

public class Result {
	private String text;
	private int polarity;

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public int getPolarity() {
		return polarity;
	}

	public void setPolarity(int polarity) {
		this.polarity = polarity;
	}

}
